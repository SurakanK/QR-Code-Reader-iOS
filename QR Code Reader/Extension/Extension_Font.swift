import UIKit

extension UIFont{
        
    static func NunitoSansBlack(size : CGFloat) -> UIFont{
        return UIFont(name: "NunitoSans-Black", size: size)!
    }
    
    static func NunitoSansRegular(size : CGFloat) -> UIFont{
        return UIFont(name: "NunitoSans-Regular", size: size)!
    }
    
    static func NunitoSansExtraBold(size : CGFloat) -> UIFont{
        return UIFont(name: "NunitoSans-ExtraBold", size: size)!
    }
    
    static func NunitoSansBold(size : CGFloat) -> UIFont{
        return UIFont(name: "NunitoSans-Bold", size: size)!
    }

}


